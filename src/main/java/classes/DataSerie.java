package classes;

public class DataSerie {
	
	private String color;
	private int y;
	
	public DataSerie(String color, int y) {
		super();
		this.color = color;
		this.y = y;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}

}
